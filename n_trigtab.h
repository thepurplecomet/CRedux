float degtorad(int deg) {
    int sign = 1;
    if (deg < 0) sign = -1;
    deg = abs(deg);
    while (deg >= 360)
        deg -= 360;
    return sign*degradtab[deg];
}
int radtodeg(float rad) {
    int sign = 1;
    if (rad < 0) {
        sign = -1;
        rad = fabs(rad);
    }
    while (rad >= 6.2832)
        rad -= 6.2832;
    unsigned int low = 0;
    unsigned int high = 359;
    while ((high-low) > 1) {
        unsigned int mid = low+((high-low)/2);
        if (degradtab[mid] < rad)
            low = mid;
        else if (degradtab[mid] > rad)
            high = mid;
        else
            return mid;
    }
    if (rad >= 6.2745)
        return 0;
    float lowdiff = rad - degradtab[low];
    if (lowdiff < 0)
        lowdiff = (-1)*lowdiff;
    float highdiff = degradtab[high] - rad;
    if (highdiff < 0)
        highdiff = (-1)*highdiff;
    return sign*(lowdiff <= highdiff ? low : high);
}
